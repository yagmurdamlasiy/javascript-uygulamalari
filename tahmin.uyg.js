let sayi=prompt("1 ile 5 arasında bir sayı giriniz:");
switch(sayi)
{
    case "1":
        alert("Girilen sayı 1'dir.");
        break;
    case "2":
        alert("Girilen sayı 2'dir.");
        break;
    case "3":
        alert("Girilen sayı 3'dir.");
        break;
    case "4":
        alert("Girilen sayı 4'tür.");
        break;
    case "5":
        alert("Girilen sayı 5'tir.");
        break;
    default:
        alert("Geçersiz sayı girişi yapılmıştır.");
        break;
}
